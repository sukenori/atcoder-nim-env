#!/usr/bin/env bash

# パイプ途中も含めて失敗、未定義変数を検出
set -euo pipefail

# このスクリプト自身のディレクトリを作業ルートにする
SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
cd "${SCRIPT_DIR}"
PARENT_DIR="$(cd "${SCRIPT_DIR}/.." && pwd)"
NIM_LIB_DIR="${PARENT_DIR}/cp-nim-lib"
SOLVED_LOG_DIR="${PARENT_DIR}/cp-solved-log"

# git が使えるか確認する
if ! command -v git >/dev/null 2>&1; then
  echo "git が見つかりません。先に git をインストールしてください。" >&2
  exit 1
fi

# docker が使えるか確認する
if ! command -v docker >/dev/null 2>&1; then
  echo "docker が見つかりません。先に Docker Engine をインストールしてください。" >&2
  exit 1
fi
# このスクリプト内では sudo docker で実行しておく
DOCKER_CMD=(sudo docker)
# docker デーモンの状態を確認
if ! "${DOCKER_CMD[@]}" info >/dev/null 2>&1; then
  echo "docker デーモンに接続できません。Docker の起動状態や権限を確認してください。" >&2
  exit 1
fi
if ! "${DOCKER_CMD[@]}" compose version >/dev/null 2>&1; then
  echo "docker compose が使えません。Docker Compose プラグインを確認してください。" >&2
  exit 1
fi
# 開発コンテナをビルドして起動する
"${DOCKER_CMD[@]}" compose up -d --build atcoder-nim

# cp-nim-lib と cp-solved-log を取得・更新（既存なら origin 補正 + ff-only pull）
ensure_repo() {
  local url="$1"
  local dir="$2"
  local current_url

  if [ -d "$dir/.git" ]; then
    if git -C "$dir" remote get-url origin >/dev/null 2>&1; then
      current_url="$(git -C "$dir" remote get-url origin)"
      if [ "$current_url" != "$url" ]; then
        git -C "$dir" remote set-url origin "$url"
      fi
    else
      git -C "$dir" remote add origin "$url"
    fi

    if ! git -C "$dir" ls-remote --exit-code --heads origin >/dev/null 2>&1; then
      echo "$dir の origin にブランチがないため pull をスキップします。" >&2
      return
    fi

    git -C "$dir" pull --ff-only
    return
  fi

  if [ -e "$dir" ]; then
    echo "$dir が既に存在しますが Git リポジトリではないため中断します。" >&2
    return 1
  fi

  git clone "$url" "$dir"
}

ensure_repo "https://github.com/sukenori/cp-nim-lib.git" "${NIM_LIB_DIR}"
ensure_repo "https://github.com/sukenori/cp-solved-log.git" "${SOLVED_LOG_DIR}"

# Telescope の優先検索スコープを更新する（優先対象は個別環境の setup.sh 側で指定する方針のため）
SCOPE_FILE="${HOME}/.config/nvim/telescope_priority_dirs.txt"
mkdir -p "$(dirname "${SCOPE_FILE}")"
{
  echo "# generated by atcoder-nim-env/setup.sh"
  echo "${SCRIPT_DIR}"
  echo "${NIM_LIB_DIR}"
  echo "${SOLVED_LOG_DIR}"
} > "${SCOPE_FILE}"

# 作業ディレクトリを作る
mkdir -p ./work
mkdir -p ./test

# bundle スクリプトに実行権限を付与
[ -f ./bundle.sh ] && chmod +x ./bundle.sh
